    public function login()
    {
        $credentials =  request()->validate([
            'user.email' => ['required', 'email'],
            'user.password' => ['required']
        ]);

        if (Auth::attempt($credentials['user'])) {
            return response()->json(auth()->user());
        }

        throw new UnauthorizedException;
    }
